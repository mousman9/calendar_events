import 'package:flutter/material.dart';

TextStyle headingText(double size) {
  return TextStyle(
      // fontFamily: "Roboto",
      color: Colors.white,
      fontSize: size,
      fontWeight: FontWeight.w800);
}

TextStyle subHeadingText(double size) {
  return TextStyle(
      // fontFamily: "Roboto",
      color: Colors.white,
      fontSize: size,
      fontWeight: FontWeight.w500);
}

TextStyle normalText(double size) {
  return TextStyle(
    // fontFamily: "Roboto",
    color: Colors.white,
    fontSize: size,
    fontWeight: FontWeight.w400,
  );
}
